create table if not exists public.line_search_documents (
  id bigint generated by default as identity primary key,
  room_id text,
  room_name text,
  storage_bucket text not null default 'line-documents',
  storage_path text not null,
  original_file_name text not null,
  mime_type text not null,
  file_size_bytes bigint not null default 0,
  extracted_text text not null default '',
  source text not null default 'manual_upload',
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),
  constraint line_search_documents_source_check
    check (source in ('manual_upload', 'line_import', 'system_import'))
);

create unique index if not exists line_search_documents_storage_path_uidx
  on public.line_search_documents(storage_path);

create index if not exists line_search_documents_room_created_idx
  on public.line_search_documents(room_id, created_at desc);

create index if not exists line_search_documents_created_idx
  on public.line_search_documents(created_at desc);

alter table public.line_search_documents enable row level security;

do $$
begin
  if not exists (
    select 1
    from pg_policies
    where schemaname = 'public'
      and tablename = 'line_search_documents'
      and policyname = 'Service role can do everything on line_search_documents'
  ) then
    create policy "Service role can do everything on line_search_documents"
      on public.line_search_documents
      for all
      using (auth.jwt() ->> 'role' = 'service_role');
  end if;
end;
$$;

